/**
 * update-newsletter-link.js
 *
 * Uses the Mailchimp Marketing API to fetch the most recent sent campaign
 * and writes its archive URL to newsletter-link.json.
 *
 * If the latest campaign was NOT sent today (Prague time), the script sends
 * a failure/reminder email to FAIL_EMAIL and exits with an error so the
 * operator knows to run it manually once the newsletter goes out.
 *
 * Environment variables:
 *   MAILCHIMP_API_KEY  – Mailchimp API key (e.g. abc123def456-us7)
 *   GMAIL_USER         – Gmail address used to send failure email
 *   GMAIL_APP_PASSWORD – Gmail app password
 *   FAIL_EMAIL         – Recipient for failure/reminder notifications
 *   SKIP_DATE_CHECK    – Set to "true" to bypass the same-day check (for testing)
 */

const fs = require('fs');
const path = require('path');
const https = require('https');
const nodemailer = require('nodemailer');

const API_KEY = process.env.MAILCHIMP_API_KEY;
const GMAIL_USER = process.env.GMAIL_USER;
const GMAIL_APP_PASSWORD = process.env.GMAIL_APP_PASSWORD;
const FAIL_EMAIL = process.env.FAIL_EMAIL;
const SKIP_DATE_CHECK = process.env.SKIP_DATE_CHECK === 'true';
const OUTPUT_FILE = path.resolve(__dirname, '..', 'newsletter-link.json');

if (!API_KEY) {
  console.error('Error: MAILCHIMP_API_KEY environment variable is not set.');
  process.exit(1);
}

// Extract data center from API key (the part after the dash, e.g. "us7")
const dc = API_KEY.split('-').pop();

function mailchimpGet(endpoint) {
  return new Promise((resolve, reject) => {
    const options = {
      hostname: `${dc}.api.mailchimp.com`,
      path: `/3.0${endpoint}`,
      headers: {
        Authorization: `Bearer ${API_KEY}`,
      },
    };

    https
      .get(options, (res) => {
        const chunks = [];
        res.on('data', (chunk) => chunks.push(chunk));
        res.on('end', () => {
          const body = Buffer.concat(chunks).toString('utf-8');
          if (res.statusCode !== 200) {
            return reject(new Error(`Mailchimp API ${res.statusCode}: ${body}`));
          }
          try {
            resolve(JSON.parse(body));
          } catch (e) {
            reject(new Error(`Failed to parse Mailchimp response: ${e.message}`));
          }
        });
      })
      .on('error', reject);
  });
}

/**
 * Check whether a date string falls on "today" in the Europe/Prague timezone.
 */
function isSentToday(sendTimeStr) {
  const sendDate = new Date(sendTimeStr);
  const now = new Date();
  const pragueOpts = { timeZone: 'Europe/Prague', year: 'numeric', month: '2-digit', day: '2-digit' };
  const sendDatePrague = sendDate.toLocaleDateString('en-CA', pragueOpts);
  const nowPrague = now.toLocaleDateString('en-CA', pragueOpts);
  return sendDatePrague === nowPrague;
}

/**
 * Send a failure/reminder email via Gmail SMTP.
 */
async function sendFailureEmail(campaignSubject, campaignSendTime) {
  if (!GMAIL_USER || !GMAIL_APP_PASSWORD || !FAIL_EMAIL) {
    console.error('Cannot send failure email: GMAIL_USER, GMAIL_APP_PASSWORD, or FAIL_EMAIL is not set.');
    return;
  }

  const transporter = nodemailer.createTransport({
    service: 'gmail',
    auth: { user: GMAIL_USER, pass: GMAIL_APP_PASSWORD },
  });

  const html = `
    <p>The <strong>Update Newsletter Link</strong> script ran but no newsletter was sent today.</p>
    <p>The most recent campaign found:</p>
    <ul>
      <li><strong>Subject:</strong> ${campaignSubject}</li>
      <li><strong>Sent:</strong> ${campaignSendTime}</li>
    </ul>
    <p>Please send the newsletter and then run the script manually.</p>
    <p style="color:#999;font-size:12px;">Auto-generated by ICP Automation</p>
  `;

  await transporter.sendMail({
    from: `"ICP Automation" <${GMAIL_USER}>`,
    to: FAIL_EMAIL,
    subject: 'Reminder: Newsletter not sent yet – run update-newsletter-link manually',
    html,
  });

  console.log(`Failure/reminder email sent to ${FAIL_EMAIL}`);
}

const MAX_RETRIES = 6;
const RETRY_INTERVAL_MS = 30 * 60 * 1000; // 30 minutes

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

/**
 * Try to fetch today's newsletter. Returns the campaign object if found,
 * or null if the latest campaign was not sent today.
 */
async function tryFetchTodaysNewsletter() {
  console.log(`Using Mailchimp data center: ${dc}`);
  console.log('Fetching most recent sent campaign...');

  const data = await mailchimpGet(
    '/campaigns?sort_field=send_time&sort_dir=DESC&count=1&status=sent'
  );

  if (!data.campaigns || data.campaigns.length === 0) {
    console.error('Error: No sent campaigns found in Mailchimp.');
    process.exit(1);
  }

  const campaign = data.campaigns[0];
  const subject = campaign.settings?.subject_line || '(no subject)';
  const sendTime = campaign.send_time || '';

  console.log(`Latest campaign: "${subject}"`);
  console.log(`  Sent: ${sendTime}`);

  if (!SKIP_DATE_CHECK && sendTime && !isSentToday(sendTime)) {
    return null; // Not sent today
  }

  return campaign;
}

async function main() {
  let campaign = await tryFetchTodaysNewsletter();

  // Retry loop: check every 30 minutes, up to 6 times
  if (!campaign && !SKIP_DATE_CHECK) {
    for (let attempt = 1; attempt <= MAX_RETRIES; attempt++) {
      console.log(`\nNewsletter not sent yet. Retry ${attempt}/${MAX_RETRIES} in 30 minutes...`);
      await sleep(RETRY_INTERVAL_MS);
      campaign = await tryFetchTodaysNewsletter();
      if (campaign) break;
    }
  }

  if (!campaign) {
    const data = await mailchimpGet(
      '/campaigns?sort_field=send_time&sort_dir=DESC&count=1&status=sent'
    );
    const latest = data.campaigns?.[0];
    const subject = latest?.settings?.subject_line || '(no subject)';
    const sendTime = latest?.send_time || '';
    console.error(`\nNewsletter was not sent today after ${MAX_RETRIES} retries. Sending failure email.`);
    await sendFailureEmail(subject, sendTime);
    process.exit(1);
  }

  const archiveUrl = campaign.long_archive_url || campaign.archive_url;
  const subject = campaign.settings?.subject_line || '(no subject)';
  const sendTime = campaign.send_time || '';

  console.log(`  Archive URL: ${archiveUrl}`);

  if (!archiveUrl) {
    console.error('Error: Campaign has no archive_url.');
    process.exit(1);
  }

  const result = {
    url: archiveUrl,
    subject: subject,
    sendTime: sendTime,
    updatedAt: new Date().toISOString(),
  };

  fs.writeFileSync(OUTPUT_FILE, JSON.stringify(result, null, 2) + '\n', 'utf-8');
  console.log('\nUpdated newsletter-link.json successfully.');
}

main().catch((err) => {
  console.error('Fatal error:', err.message);
  process.exit(1);
});
